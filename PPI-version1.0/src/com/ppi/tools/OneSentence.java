package com.ppi.tools;

public class OneSentence {
	String PMID;
	String PA;
	String PB;
	String ITA;
	String SEN;
	
	public String getPMID() {
		return PMID;
	}
	public void setPMID(String pMID) {
		PMID = pMID;
	}
	public String getPA() {
		return PA;
	}
	public void setPA(String pA) {
		PA = pA;
	}
	public String getPB() {
		return PB;
	}
	public void setPB(String pB) {
		PB = pB;
	}
	public String getITA() {
		return ITA;
	}
	public void setITA(String iTA) {
		ITA = iTA;
	}
	public String getSEN() {
		return SEN;
	}
	public void setSEN(String sEN) {
		SEN = sEN;
	}
	
}